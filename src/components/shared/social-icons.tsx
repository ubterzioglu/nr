export function SocialIcon({
  icon,
  className,
  brand = false,
  uid,
}: {
  icon: SocialPlatform;
  className?: string;
  brand?: boolean;
  uid?: string;
}) {
  if (brand) {
    return <SocialBrandLogo icon={icon} className={className} uid={uid} />;
  }

  switch (icon) {
    case "instagram":
      return <InstagramIcon className={className} />;
    case "youtube":
      return <YoutubeIcon className={className} />;
    case "linkedin":
      return <LinkedinIcon className={className} />;
    case "whatsapp":
      return <WhatsappIcon className={className} />;
    default:
      return <MessageCircle className={className} />;
  }
}

